const FONT_NAMES = ["Standard", "Big", "Bubble", "Digital", "Slant"];

export function generateAscii(text, fontName) {
  const font = FONTS[fontName] || FONTS.Standard;
  const lines = text.toUpperCase().split("");
  const height = font.height;
  const result = [];

  for (let row = 0; row < height; row++) {
    let line = "";
    for (const ch of lines) {
      const glyph = font.chars[ch];
      if (glyph && glyph[row] !== undefined) {
        line += glyph[row];
      } else {
        line += " ".repeat(font.chars[" "] ? font.chars[" "][row].length : 2);
      }
    }
    result.push(line.replace(/\s+$/, ""));
  }

  return result.join("\n");
}

export function getFonts() {
  return [...FONT_NAMES];
}

