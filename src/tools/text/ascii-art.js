export function render(container) {
  container.innerHTML = `
    <div class="tool-layout">
      <div style="border:1px solid var(--color-border);border-radius:var(--radius-md);padding:var(--space-4);margin-bottom:var(--space-4);">
        <div class="form-group">
          <label>Your Text</label>
          <input type="text" id="aa-input" class="text-input" placeholder="Enter text to convert..." value="Hello!">
        </div>
        <div class="form-group">
          <label>Font Style</label>
          <select id="aa-font" class="text-input" style="max-width:300px;">
            ${FONT_NAMES.map(f => `<option value="${f}" ${f === "Standard" ? "selected" : ""}>${f}</option>`).join("")}
          </select>
        </div>
        <button class="btn btn-primary btn-lg" id="aa-generate" style="width:100%;">Generate ASCII Art</button>
      </div>
      <div id="aa-output" style="border:1px solid var(--color-border);border-radius:var(--radius-md);padding:var(--space-4);min-height:120px;overflow:auto;font-family:monospace;font-size:10px;line-height:1.1;white-space:pre;background:#000;color:#0f0;"></div>
      <div style="display:flex;gap:var(--space-2);margin-top:var(--space-2);flex-wrap:wrap;">
        <button class="btn btn-secondary" id="aa-copy">Copy to Clipboard</button>
        <button class="btn btn-secondary" id="aa-download">Download .txt</button>
      </div>
    </div>
  `;

  const input = container.querySelector("#aa-input");
  const fontSelect = container.querySelector("#aa-font");
  const output = container.querySelector("#aa-output");
  const generateBtn = container.querySelector("#aa-generate");
  const copyBtn = container.querySelector("#aa-copy");
  const downloadBtn = container.querySelector("#aa-download");

  function generate() {
    const text = input.value || " ";
    const font = fontSelect.value;
    const result = generateAscii(text, font);
    output.textContent = result || "(no output for this font combination)";
  }

  generateBtn.addEventListener("click", generate);

  copyBtn.addEventListener("click", () => {
    const text = output.textContent;
    if (!text || text === "(no output for this font combination)") return;
    navigator.clipboard.writeText(text).catch(() => {});
  });

  downloadBtn.addEventListener("click", () => {
    const text = output.textContent;
    if (!text || text === "(no output for this font combination)") return;
    const blob = new Blob([text], { type: "text/plain" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = "ascii-art.txt";
    a.click();
    URL.revokeObjectURL(url);
  });

  generate();
}

